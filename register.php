                    // Insert into login table using prepared statement
                    $login_stmt = mysqli_prepare($con, 
                        "INSERT INTO login (username, password, type, status) VALUES (?, ?, ?, ?)");
                    
                    mysqli_stmt_bind_param($login_stmt, "ssss", $email, $hashed_password, $user_type, $status);
                    
                    if (mysqli_stmt_execute($login_stmt)) {
                        $success_message = 'Registration successful! Your account is pending approval.';
                        // Clear form data
                        $_POST = array();
                    } else {
                        $error_message = 'Registration failed. Please try again.';
                    }
                    mysqli_stmt_close($login_stmt);
                } else {
                    $error_message = 'Registration failed. Please try again.';
